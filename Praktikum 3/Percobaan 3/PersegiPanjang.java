public class PersegiPanjang {
    public int panjang;
    public int lebar;
    public PersegiPanjang (int p, int l) {
        panjang = p;
        lebar = l;
    }
    public void cetakInfo() {
        System.out.println(", panjang : " + (panjang) 
        + ", lebar : " + (lebar));
    }
}